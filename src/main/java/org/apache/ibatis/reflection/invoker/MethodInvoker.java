package org.apache.ibatis.reflection.invoker;

import java.lang.reflect.InvocationTargetException;
import java.lang.reflect.Method;

/**
 * 方法执行器，该方法执行器中，{@link MethodInvoker#method}用于存储实际需要执行的方法。
 * 而{@link MethodInvoker#type}类型则会根据实际{@link MethodInvoker#method}的参数{@link Method#getParameterTypes()}是否为{@code 1}
 * 来判断，如果{@link Method#getParameterTypes()} > 1的情况下，则去{@link Method#getReturnType()}作为{@link MethodInvoker#type}的值
 *
 * @author Clinton Begin
 */
public class MethodInvoker implements Invoker {

    private final Class<?> type;
    private final Method method;

    public MethodInvoker(Method method) {
        this.method = method;

        if (method.getParameterTypes().length == 1) {
            type = method.getParameterTypes()[0];
        }
        else {
            type = method.getReturnType();
        }
    }

    @Override
    public Object invoke(Object target, Object[] args) throws IllegalAccessException, InvocationTargetException {
        return method.invoke(target, args);
    }

    @Override
    public Class<?> getType() {
        return type;
    }
}
